# GCD and LCM ( Part 1)

# Create a function that takes two numbers as arguments and returns the Greatest Common Divisor (GCD) of the two numbers.
# Examples

def gcd(num1, num2)
  num1.gcd(num2)
end

puts gcd(3, 5)  #➞ 1

puts gcd(14, 28)  #➞ 14

puts gcd(4, 18)  #➞ 2

# Notes

# The GCD is the highest number that can divide both arguments without a remainder.